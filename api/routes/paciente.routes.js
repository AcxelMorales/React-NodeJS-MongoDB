const express = require('express');

const pacienteController = require('../controllers/paciente.controller');

const router = express.Router();

///////////////////////////////////////////////////////////////////////////////////////
// <= GET ALL =>
///////////////////////////////////////////////////////////////////////////////////////
router.get('/pacientes', pacienteController.getAll);

///////////////////////////////////////////////////////////////////////////////////////
// <= GET BY ID =>
///////////////////////////////////////////////////////////////////////////////////////
router.get('/pacientes/:id', pacienteController.getById);

///////////////////////////////////////////////////////////////////////////////////////
// <= POST =>
///////////////////////////////////////////////////////////////////////////////////////
router.post('/pacientes', pacienteController.post);

///////////////////////////////////////////////////////////////////////////////////////
// <= PUT =>
///////////////////////////////////////////////////////////////////////////////////////
router.put('/pacientes/:id', pacienteController.put);

///////////////////////////////////////////////////////////////////////////////////////
// <= DELETE =>
///////////////////////////////////////////////////////////////////////////////////////
router.delete('/pacientes/:id', pacienteController.delete);

module.exports = router;